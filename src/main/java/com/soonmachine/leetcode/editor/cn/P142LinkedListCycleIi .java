/**
 * Definition for singly-linked list.
 * class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) {
 *         val = x;
 *         next = null;
 *     }
 * }
 */
public class Solution {
    public ListNode detectCycle(ListNode head) {
        if (head == null || head.next == null){
            return null;
        }
        ListNode fast = head;
        ListNode slow = head;
        while (fast.next != null){
            fast = fast.next.next;
            slow = slow.next;
            if (fast == null){
                return null;
            }
            if (fast == slow){
                ListNode ans = head;
                while (ans != fast){
                    ans = ans.next;
                    fast = fast.next;
                }
                return ans;
            }
        }
        return null;
    }
}
